package area;

public class rectangle2 {
	
	private int width;
	private int height;

	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	
	public void setHeight(int height) {
		this.height = height;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int process(int a, int b) {
		return a * b;
	}
	
}
